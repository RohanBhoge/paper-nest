/**
 * Gets notifications for an organization.
 */
async function getOrganizationNotifications(userId) {
  let connection;
  try {
    connection = await pool.getConnection();

    const today = new Date().toISOString().split('T')[0];

    // 1. Lazy Deletion: Delete expired notifications first
    await connection.execute(`DELETE FROM notifications WHERE event_date < ?`, [
      today,
    ]);

    // 2. Fetch remaining valid notifications
    const query = `
            SELECT id, content, event_date, created_at
            FROM notifications
            WHERE user_id = ? AND (event_date IS NULL OR event_date >= ?)
            ORDER BY event_date ASC, created_at DESC
        `;

    const [rows] = await connection.execute(query, [userId, today]);
    return rows;
  } catch (error) {
    console.error('DB Error in getOrganizationNotifications:', error);
    throw error;
  } finally {
    if (connection) connection.release();
  }
}

