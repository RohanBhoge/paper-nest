/**
 * Stores a new paper for a user.
 */
async function storeNewPaperForUser(userId, data) {
  await ensureTablesExist();

  let connection;
  try {
    connection = await pool.getConnection();

    const insertQuery = `
      INSERT INTO question_papers (
        user_id, paper_id, exam_name, class, subject, exam_date, marks, duration,
        paper_questions, paper_answers, metadata
      ) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
    `;

    // Always stringify questions, answers & metadata
    const safePaperQuestions = JSON.stringify(data.paper_questions || []);
    const safePaperAnswers = JSON.stringify(data.paper_answers || []);
    const safeMetadata = JSON.stringify(data.metadata || {});

    const values = [
      userId,
      data.paper_id,
      data.exam_name ?? null,
      data.class ?? null,
      data.subject ?? null,
      data.exam_date ?? null,
      data.marks ?? null,
      data.duration ?? 0,
      safePaperQuestions,
      safePaperAnswers,
      safeMetadata,
    ];

    const [result] = await connection.execute(insertQuery, values);
    return result.insertId;
  } finally {
    if (connection) connection.release();
  }
}

