/**
 * Ensures necessary tables exist in the database.
 */
async function ensureTablesExist() {
  let connection;
  try {
    connection = await pool.getConnection();

    // 1. Table for primary users (Admins/Teachers)
    await connection.execute(`
      CREATE TABLE IF NOT EXISTS users (
        id INT AUTO_INCREMENT PRIMARY KEY,
        email VARCHAR(255) NOT NULL UNIQUE,
        password_hash VARCHAR(255) NOT NULL,
        username VARCHAR(255) UNIQUE, 
        full_name VARCHAR(255),
        watermark VARCHAR(255) DEFAULT NULL,
        logo VARCHAR(255) DEFAULT NULL,
        class_name VARCHAR(255) DEFAULT NULL,
        created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
        updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
        is_active BOOLEAN DEFAULT TRUE
      )
    `);

    // 2. Table for question papers generated by users
    await connection.execute(`
      CREATE TABLE IF NOT EXISTS question_papers (
        id INT AUTO_INCREMENT PRIMARY KEY,
        user_id INT NOT NULL,
        paper_id VARCHAR(100) UNIQUE,
        exam_name VARCHAR(50),
        class VARCHAR(50),
        subject VARCHAR(50),
        exam_date DATE,
        marks INT,
        duration INT DEFAULT 0,
        paper_questions LONGTEXT, 
        paper_answers LONGTEXT, 
        paper_solution LONGTEXT,
        metadata LONGTEXT,
        created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
        updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
        FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE CASCADE
      )
    `);

    // 3. Table for student accounts, linked to an Admin user_id
    await connection.execute(`
      CREATE TABLE IF NOT EXISTS students (
        id INT AUTO_INCREMENT PRIMARY KEY,
        user_id INT NOT NULL,
        user_name VARCHAR(255) NOT NULL,
        email VARCHAR(255) NOT NULL UNIQUE,
        password_hash VARCHAR(255) NOT NULL,
        full_name VARCHAR(255),
        std VARCHAR(50),
        class VARCHAR(50),
        created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
        updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
        FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE CASCADE
      )
    `);

    // 4. Table for organizational notifications
    await connection.execute(`
      CREATE TABLE IF NOT EXISTS notifications (
        id INT AUTO_INCREMENT PRIMARY KEY,
        user_id INT NOT NULL,
        content TEXT NOT NULL,
        event_date DATE,
        created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
        FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE CASCADE
      )
    `);
    console.log('Database schema checked/initialized successfully.');
  } catch (error) {
    console.error('Error during database schema initialization:', error);
    throw error;
  } finally {
    if (connection) connection.release();
  }
}

/**
 * Creates a new user.
 */
async function createUser(email, password_hash, full_name, watermark, logokey) {
  await ensureTablesExist();

  let connection;
  try {
    connection = await pool.getConnection();
    const [cols] = await connection.execute(
      `SELECT COLUMN_NAME FROM INFORMATION_SCHEMA.COLUMNS WHERE TABLE_SCHEMA = ? AND TABLE_NAME = 'users'`,
      [DB_NAME]
    );

    const colSet = new Set(cols.map((c) => c.COLUMN_NAME));

    const insertCols = [];
    const placeholders = [];
    const values = [];

    if (colSet.has('email')) {
      insertCols.push('email');
      placeholders.push('?');
      values.push(email);
    }
    if (colSet.has('password_hash')) {
      insertCols.push('password_hash');
      placeholders.push('?');
      values.push(password_hash);
    }

    if (colSet.has('username')) {
      let username = null;
      let base = 'user';
      if (full_name && String(full_name).trim())
        base = String(full_name).trim().split(' ')[0];
      else if (email && String(email).includes('@'))
        base = String(email).split('@')[0];

      // Append random number to ensure uniqueness
      username = `${base}_${Math.floor(Math.random() * 90000) + 10000}`;

      insertCols.push('username');
      placeholders.push('?');
      values.push(username);
    }

    if (colSet.has('full_name')) {
      insertCols.push('full_name');
      placeholders.push('?');
      values.push(full_name);
    }

    if (colSet.has('watermark')) {
      insertCols.push('watermark');
      placeholders.push('?');
      values.push(watermark);
    }

    if (colSet.has('logo')) {
      insertCols.push('logo');
      placeholders.push('?');
      values.push(logokey);
    }

    if (insertCols.length === 0) {
      throw new Error(
        'No known user columns present in database to insert into.'
      );
    }

    const sql = `INSERT INTO users (${insertCols.join(
      ','
    )}) VALUES (${placeholders.join(',')})`;
    const [result] = await connection.execute(sql, values);
    return result.insertId;
  } finally {
    if (connection) connection.release();
  }
}

/**
 * Retrieves a user by email.
 */
async function getUserByEmail(email) {
  let connection;
  try {
    connection = await pool.getConnection();
    try {
      const [rows] = await connection.execute(
        `SELECT id, email, password_hash, full_name, is_active, created_at, watermark, class_name, logo FROM users WHERE email = ? LIMIT 1`,
        [email]
      );
      return rows.length ? rows[0] : null;
    } catch (err) {
      if (err && err.code === 'ER_BAD_FIELD_ERROR') {
      // Fallback if some columns are missing
        const [rows2] = await connection.execute(
          `SELECT id, email, password_hash, full_name, logo, watermark, class_name, created_at FROM users WHERE email = ? LIMIT 1`,
          [email]
        );
        if (!rows2.length) return null;
        const r = rows2[0];
        return {
          id: r.id,
          email: r.email,
          password_hash: r.password_hash,
          full_name: r.full_name,
          created_at: r.created_at,
          watermark: r.watermark,
          class_name: r.class_name,
          logo: r.logo,
        };
      }
      throw err;
    }
  } finally {
    if (connection) connection.release();
  }
}

